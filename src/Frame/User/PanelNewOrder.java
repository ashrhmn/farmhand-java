    private void placeOrderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_placeOrderBtnActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) cartTable.getModel();
        DefaultTableModel iModel = (DefaultTableModel) invoiceTable.getModel();
        if (model.getRowCount() != 0) {
            addToCartBtn.setEnabled(false);
            placeOrderBtn.setEnabled(false);
            removeCartItemBtn.setEnabled(false);
            clearCartBtn.setEnabled(false);
            amountTf.setEditable(false);

            Order order = new Order();
            order.setUser(user);
            order.setDate(dtf.format(date));

            for (int i = 0; i < model.getRowCount(); ++i) {
                OrderItems item = new OrderItems();
                item.setProduct(new Product());
                item.getProduct().setName(model.getValueAt(i, 0).toString());
                item.getProduct().setPrice(Double.parseDouble(model.getValueAt(i, 2).toString()));
                item.setAmount(Double.parseDouble(model.getValueAt(i, 1).toString()));
                Object[] rowData = {model.getValueAt(i, 0), model.getValueAt(i, 1), model.getValueAt(i, 2), (Double.parseDouble(model.getValueAt(i, 1).toString()) * Double.parseDouble(model.getValueAt(i, 2).toString()))};
                iModel.addRow(rowData);
                order.getItems().add(item);
            }
            order.setTotalBillPrice();
            order.setPaidAmount(0.0);
            order.setDeliveryStatus("TBA");
            order.setPaymentStatus("Incomplete");
            order.setOrderStatus("Incomplete");
            order.setId(or.generateNewOrderId());
            order.setAgent(new User());
            order.setDeliveredVy(new User());

            totalPriceTf.setText(order.getTotalBillPrice().toString());
            dateTf.setText(order.getDate());
            nameTf.setText(order.getUser().getPerson().getName());
            addressTf.setText(order.getUser().getPerson().getAddress());
            phoneTf.setText(order.getUser().getPerson().getMobile());
            paidAmountTf.setText(order.getPaidAmount().toString());
            dueAmountTf.setText(String.valueOf(order.getTotalBillPrice() - order.getPaidAmount()));
            deliveryStatusTf.setText(order.getDeliveryStatus());
            orderStatusTf.setText(order.getOrderStatus());
            orderIdTf.setText(order.getId().toString());

            or.saveNewOrder(order);
        }
        else{
            JOptionPane.showMessageDialog(this, "Your Cart is Empty", "Error", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_placeOrderBtnActionPerformed

    private void addToCartBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addToCartBtnActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = availableItemsTable.getSelectedRows();
        Double amount = Double.parseDouble(amountTf.getText());
        Double previousAmount;
        DefaultTableModel model = (DefaultTableModel) availableItemsTable.getModel();
        DefaultTableModel iModel = (DefaultTableModel) cartTable.getModel();
        for (int i = 0; i < selectedRows.length; ++i) {
            previousAmount = 0.0;
            for (int j = 0; j < iModel.getRowCount(); ++j) {
                if (model.getValueAt(selectedRows[i], 0).equals(iModel.getValueAt(j, 0))) {
                    previousAmount = Double.parseDouble(iModel.getValueAt(j, 1).toString());
                    iModel.removeRow(j);
                }
            }
            Object[] rowData = {model.getValueAt(selectedRows[i], 0), amount + previousAmount, model.getValueAt(selectedRows[i], 2),model.getValueAt(i, i)};
            iModel.addRow(rowData);
        }

    }//GEN-LAST:event_addToCartBtnActionPerformed

