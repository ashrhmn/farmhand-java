    public void updayeAvailableItemTable() {
        DefaultTableModel model = (DefaultTableModel) availableItemsTable.getModel();
        model.setRowCount(0);
        products.clear();

        products = pr.allAvailableProduct();

        for (int i = 0; i < products.size(); ++i) {
            model.addRow(availableItemRowData(products.get(i)));
        }
    }

    public void updayeAvailableItemTable(String category) {
        DefaultTableModel model = (DefaultTableModel) availableItemsTable.getModel();
        model.setRowCount(0);
        products.clear();

        products = pr.allAvailableProduct();

        for (int i = 0; i < products.size(); ++i) {
            if (products.get(i).getCategory().equals(category)) {
                model.addRow(availableItemRowData(products.get(i)));
            }
        }
    }

    public void setUpComboBoxItems() {

//        categoryComboBox.removeAllItems();
        products.clear();
        products = pr.allAvailableProduct();

        ArrayList<String> comboItems = new ArrayList<>();
        comboItems.clear();

        for (int i = 0; i < products.size(); ++i) {
            if (comboItems.contains(products.get(i).getCategory())) {

            } else {
                comboItems.add(products.get(i).getCategory());
            }
        }
//        categoryComboBox.addItem("All");
        for (int i = 0; i < comboItems.size(); ++i) {
            categoryComboBox.addItem(comboItems.get(i));
        }
    }

    public Object[] availableItemRowData(Product product) {
        Object[] rowdata = {product.getName(), product.getCategory(), product.getPrice()};
        return rowdata;
    }

